public class LibraryList {

    private String myLibrary;

    private String myLogin;

    private String search;

    private String readerRecommend;

    private String hotMessage;

    private String history;

    private String searchResult;

    public String getSearchResult() {
        return searchResult;
    }

    public void setSearchResult(String searchResult) {
        this.searchResult = searchResult;
    }

    public String getMyLibrary() {
        return myLibrary;
    }

    public void setMyLibrary(String myLibrary) {
        this.myLibrary = myLibrary;
    }

    public String getMyLogin() {
        return myLogin;
    }

    public void setMyLogin(String myLogin) {
        this.myLogin = myLogin;
    }

    public String getSearch() {
        return search;
    }

    public void setSearch(String search) {
        this.search = search;
    }

    public String getReaderRecommend() {
        return readerRecommend;
    }

    public void setReaderRecommend(String readerRecommend) {
        this.readerRecommend = readerRecommend;
    }

    public String getHistory() {
        return history;
    }

    public void setHistory(String history) {
        this.history = history;
    }

    public String getHotMessage() {
        return hotMessage;
    }

    public void setHotMessage(String hotMessage) {
        this.hotMessage = hotMessage;
    }
}
